
# instalar o RStudio =======================================================

instalar_rstudio () { 
  # nome, com extensão, do pacote ------------------------------------------
  rstudio_deb=$(basename "$URL_RSTUDIO")
  echo 
  echo "##### Instalando o RStudio ====================================================="
  echo 
  #-------------------------------------------------------------------------
  cd "$TMP_INSTALACAO" || { echo "Erro na construção do diretório"; exit 1; }
  #-------------------------------------------------------------------------
  echo " # fazendo o download..."
  #-------------------------------------------------------------------------
  wget "$URL_RSTUDIO" 
  #-------------------------------------------------------------------------
  echo 
  echo " # instalando o programa..."
  echo 
  #-------------------------------------------------------------------------
  sudo dpkg -i "$rstudio_deb"
  #-------------------------------------------------------------------------
  echo 
  echo "----- FEITO (17 / $TOTAL) ----------------------------------------------------------"
  #-------------------------------------------------------------------------
}

instalar_rstudio
